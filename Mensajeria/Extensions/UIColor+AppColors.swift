import Foundation

extension UIColor {
    class func getPrimaryAppColor() -> UIColor {
        return UIColor(red: 28.0/255.0, green: 189.0/255.0, blue: 211.0/255.0, alpha: 1.0)
    }
    
    class func getSecondaryAppColor() -> UIColor {
        return UIColor(red: 15.0/255.0, green: 95.0/255.0, blue: 106.0/255.0, alpha: 1.0)
    }
    
    class func getThirdAppColor() -> UIColor {
        return UIColor(red: 236.0/255.0, green: 255.0/255.0, blue: 83.0/255.0, alpha: 1.0)
    }
}
